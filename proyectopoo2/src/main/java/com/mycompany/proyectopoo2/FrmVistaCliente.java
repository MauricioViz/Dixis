    private void GrillaClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_GrillaClienteMouseClicked
        if(evt.getClickCount() == 1)
        {
            int fila = GrillaCliente.getSelectedRow();
            FrmVenta.txtCodCliVenta.setText(GrillaCliente.getValueAt(fila, 0).toString());
            FrmVenta.txtRazSocCliVenta.setText(GrillaCliente.getValueAt(fila, 1).toString());
            dispose();
        }
    }//GEN-LAST:event_GrillaClienteMouseClicked

