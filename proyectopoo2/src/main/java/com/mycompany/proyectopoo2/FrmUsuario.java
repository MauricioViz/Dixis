    private void btnAgregarEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarEmpActionPerformed
        if(!this.txtCodEmp.getText().isEmpty() && !this.txtNomEmp.getText().isEmpty() &&
           !this.txtApeEmp.getText().isEmpty() && !this.txtDniEmp.getText().isEmpty() &&
           !this.txtTlfEmp.getText().isEmpty())
        {
           int msg = JOptionPane.showConfirmDialog(this.btnAgregarEmp,"Deseas Guardar el Registro!!!","Guardar",JOptionPane.YES_NO_OPTION);
            if (msg == JOptionPane.YES_OPTION)
            {
                Empleado emp = new Empleado();
                emp.setIdEmp(this.txtCodEmp.getText());
                emp.setNomEmp(this.txtNomEmp.getText());
                emp.setApeEmp(this.txtApeEmp.getText());
                emp.setDniEmp(this.txtDniEmp.getText());
                emp.setTlfEmp(this.txtTlfEmp.getText());
                emp.setSexoEmp(this.cmbSexoEmp.getSelectedItem().toString());
                empController.addEmpleadoController(emp);
                listarEmp();//Actualizar la BD
                limpiarEmp();
                JOptionPane.showMessageDialog(this,"Registro Guardado Satisfactoriamente");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Llene todos los recuadros");
        }
    }//GEN-LAST:event_btnAgregarEmpActionPerformed

