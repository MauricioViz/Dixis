    public void listarEmp()
    {
        List<Empleado> lst = empController.getAllEmpleadoController();
        dtmEmpleado.setNumRows(0);
        for(Empleado x:lst)
        {
            Object[] vector = new Object[6];
            vector[0] = x.getIdEmp();
            vector[1] = x.getNomEmp();
            vector[2] = x.getApeEmp();
            vector[3] = x.getDniEmp();
            vector[4] = x.getTlfEmp();
            vector[5] = x.getSexoEmp();
            dtmEmpleado.addRow(vector);
        }
        GrillaEmp.setModel(dtmEmpleado);
    }
    
    public void listarUsu()
    {
        List<Usuario> lst = usuController.getAllUsuarioController();
        dtmUsuario.setNumRows(0);
        for(Usuario x:lst)
        {
            Object[] vector = new Object[4];
            vector[0] = x.getIdUsu();
            vector[1] = x.getIdEmpUsu();
            vector[2] = x.getNomUsu();
            vector[3] = x.getPassUsu();
            dtmUsuario.addRow(vector);
        }
        GrillaUsu.setModel(dtmUsuario);
    }
    
