    private void txtBuscarEmpKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarEmpKeyReleased
        Empleado obj = new Empleado();
        obj.setNomEmp(this.txtBuscarEmp.getText());
        List<Empleado> lst = empController.getBuscarNombreEmpleadoController(obj);
        dtmEmpleado.setNumRows(0);
        for(Empleado x:lst)
        {
            Object[] vector = new Object[6];
            vector[0] = x.getIdEmp();
            vector[1] = x.getNomEmp();
            vector[2] = x.getApeEmp();
            vector[3] = x.getDniEmp();
            vector[4] = x.getTlfEmp();
            vector[5] = x.getSexoEmp();
            dtmEmpleado.addRow(vector);
        }
        GrillaEmp.setModel(dtmEmpleado);
    }//GEN-LAST:event_txtBuscarEmpKeyReleased

